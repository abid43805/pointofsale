  public void showCustomersInTable()
    {
        StocksService service  = new StocksService();
        listOfCustomers = service.fetchCustomersList();
        DefaultTableModel model = (DefaultTableModel)tbl_Customers.getModel();
        // clear jtable content
        model.setRowCount(0);
        Object[] row = new Object[5];
        for(int i = 0; i < listOfCustomers.size(); i++)
        {
            row[0] = listOfCustomers.get(i).getCustomerId();
            row[1] = listOfCustomers.get(i).getCustomerName();
            row[2] = listOfCustomers.get(i).getCnic();
            row[3] = listOfCustomers.get(i).getMobileNo();
            row[4] = listOfCustomers.get(i).getAddress();
            
            
            model.addRow(row);
        }
    }
