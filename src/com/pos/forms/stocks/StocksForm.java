    private void btn_CustomerInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_CustomerInsertActionPerformed

        if(txt_CustomerName.getText().equals(""))
        {
            JOptionPane.showMessageDialog(tab_Stocks,"Invalid Customer Name");

        }
        
         else
         {
        
           Customer customer = new Customer();
           customer.setCustomerName(txt_CustomerName.getText());
           customer.setCnic(txt_CustomerCnic.getText());
           customer.setAddress(txt_CustomerAddress.getText());
           customer.setMobileNo(txt_CustomerMobileNo.getText());
           StocksService service  = new StocksService();
           if(service.insertCustomer(customer))
           {
              System.out.println("customer inserted succefully : " + customer.getCustomerName());
              showCustomersInTable();
              JOptionPane.showMessageDialog(tab_Stocks,"Customer Inserted Successfully .");
              clearCustomerFormFields();
                         
           }
           else
           {
                JOptionPane.showMessageDialog(tab_Stocks,"Customer Insertion Failed.");
                clearCustomerFormFields();
           }
           
         }
           
        
    }//GEN-LAST:event_btn_CustomerInsertActionPerformed

